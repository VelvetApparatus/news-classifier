NEWS_STOPWORDS = {
    "год", "года", "году", "лет", "также", "время", "ранее", "словам",
    "сообщает", "сообщила", "сообщили", "рассказал", "заявил", "отметил",
    "добавил", "уточнил", "стало", "стать", "который", "которые",
    "новость", "риа", "тасс", "интерфакс",
    "свой", "новый", "первый"
}

CUSTOM_STOPWORDS = RUS_STOPWORDS | NEWS_STOPWORDS


def clean_text(text: str, needs_lower=True) -> str:
    if not isinstance(text, str):
        return ""

    if needs_lower:
        text = text.lower()
    text = re.sub(r"http\S+|www\S+", " ", text)
    text = re.sub(r"[^a-zа-яё\s]", " ", text, flags=re.IGNORECASE)
    text = re.sub(r"\s+", " ", text).strip()

    tokens = []
    for token in text.split():
        if len(token) < 3:
            continue
        if token in CUSTOM_STOPWORDS:
            continue
        tokens.append(token)

    return " ".join(tokens)


def preprocess_texts(texts: Iterable[str], needs_lower=True) -> List[str]:
    return [clean_text(text, needs_lower) for text in texts]
